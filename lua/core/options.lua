vim.g.mapleader = " "    -- set mapleader to <space>
vim.opt.clipboard = "unnamedplus"    -- enable clipboard
vim.opt.termguicolors = true    -- enable 24-bit colour
vim.opt.showmode = false    -- don't show current mode

vim.opt.tabstop = 2    -- set tab length to 2
vim.opt.shiftwidth = 2    -- set auto indent to 2
vim.opt.expandtab = true    -- convert tab into spaces

vim.opt.number = true    -- show line numbers
vim.opt.relativenumber = true    -- show relative line numbers

vim.opt.scrolloff = 999    -- keep the cursor in the middle when scrolling

vim.opt.splitright = true;    -- vertical splits are made to the right
vim.opt.splitbelow = true;    -- horizontal splits are made below

vim.opt.linebreak = true    -- don't split words when wrapping

